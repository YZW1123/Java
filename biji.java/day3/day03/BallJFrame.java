package day03;

import java.awt.Graphics;

import javax.swing.JFrame;
import javax.swing.JPanel;
//继承窗口类,就变成了窗口类的儿子
public class BallJFrame extends JFrame{
	//BallJFrame这个类的构造方法
	public BallJFrame(){
		//this指代当前类自己的对象
		this.setBounds(50, 50, 400, 600);//设置大小和位置
		this.setVisible(true);//让窗口显示
		this.setResizable(false);//禁止变大变小
		this.setTitle("球球大作战");//设置标题
		//点击X号默认关闭
		this.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
		BallJPanel bj=new BallJPanel();//创建出画布类的对象
		this.add(bj);
		bj.moveBall();
	}
	/*主线程*/
	//构造方法如何执行?  创建某个类的对象的时候就执行了
	public static void main(String[] args) {
		new BallJFrame();//调用BallJFrame这个类的构造方法
	}
}
//看到首字母为大写的就ctrl+shift+o
//没事儿就按alt+/
class BallJPanel extends JPanel{
	int x=100;int y=200;int d=80;
	//定义一个变量用来控制球的移动方向
	int f=3;
	//作用域就是大括号{}
	//Java当中尽量不要直接使用数字,而应该使用变量的形式
	@Override
	public void paint(Graphics g) {
		super.paint(g);
		g.fillOval(x, y, d, d);
	}
	//自定义一个方法用来创建线程
	public void moveBall(){
		new Thread(){
			public void run(){
				//创建循环
				while(true){
					if(f==0){	//左上
						x--;	y--;
					}
					if(f==1){	//右上
						x++;	y--;
					}
					if(f==2){	//右下
						x++;	y++;
					}
					if(f==3){	//左下
						x--; 	y++;
					}
					if(x<=0){	//左边	//小球在哪条边上
						if(f==0){
							f=1;
						}else{
							f=2;
						}
					}
					if(x>=400-d){		//右边
						if(f==1){
							f=0;
						}else{
							f=3;
						}
					}
					if(y<=0){				//上边
						if(f==0){
							f=3;
						}else{
							f=2;
						}
					}
					if(y>=600-d-30){				//下边
						if(f==2){
							f=1;
						}else{
							f=0;
						}
					}
					//重复画
					repaint();
					//让线程慢一点
					try {
						Thread.sleep(10);
					} catch (InterruptedException e) {
						e.printStackTrace();
					}
				}
			}
		}.start();
	}
}

















